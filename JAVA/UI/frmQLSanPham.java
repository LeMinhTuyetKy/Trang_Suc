public  void fillDataToTable()
    {
        SanPhamDAO spDAO = new SanPhamDAO();
        listSP = spDAO.DocTatCaSanPham();
        tblModel = (DefaultTableModel) tblSanPham.getModel();
        tblModel.setRowCount(0);
        for( SanPham sp : listSP)
        {
            Object[] row = new Object[]{sp.getMaDM(),sp.getMaSP(),sp.getHinhAnh(), sp.getTenSP(), sp.getSoLuong(), sp.getMoTa(), sp.getGiaTien()};
             tblModel.addRow(row);
             tblSanPham.setModel(tblModel);
        }
    }
    
