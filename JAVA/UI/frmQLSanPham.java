    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        SanPhamDAO spDAO = new SanPhamDAO();
        SanPham sp = new SanPham();

        sp.setMaDM(txtMaDM.getText());
        sp.setMaSP(txtMaSP.getText());
        sp.setHinhAnh(txtHinhAnh.getText());
        sp.setTenSP(txtTenSP.getText());
        sp.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
        sp.setMoTa(txtMoTa.getText());
        sp.setGiaTien(Integer.parseInt(txtGiaTien.getText()));

        int x = spDAO.themSP(sp);
        if(x>0){
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            fillDataToTable();
        }
        else{
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
        }

    }//GEN-LAST:event_btnLuuActionPerformed

    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        int row = tblSanPham.getSelectedRow();
        txtMaDM.setText(tblSanPham.getValueAt(row,0).toString());
        txtMaSP.setText(tblSanPham.getValueAt(row,1).toString());
        txtHinhAnh.setText(tblSanPham.getValueAt(row, 2).toString());
        txtTenSP.setText(tblSanPham.getValueAt(row,3).toString());

        int SoLuong = Integer.parseInt(tblSanPham.getValueAt(row, 4).toString());
        txtSoLuong.setText(String.valueOf(SoLuong));

        txtMoTa.setText(tblSanPham.getValueAt(row,5).toString());
        int GiaTien = Integer.parseInt(tblSanPham.getValueAt(row, 6).toString());
        txtGiaTien.setText(String.valueOf(GiaTien));

    }//GEN-LAST:event_tblSanPhamMouseClicked

    private void btXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaActionPerformed
        SanPhamDAO spDAO = new SanPhamDAO();
        int x= spDAO.xoaSP((txtMaSP.getText()));
        if(x>0){
            fillDataToTable();
            JOptionPane.showMessageDialog(this, "Xóa thành công");
            clear();
        }
        else{
            JOptionPane.showMessageDialog(this, "Xóa thành công");
        }

    }//GEN-LAST:event_btXoaActionPerformed

