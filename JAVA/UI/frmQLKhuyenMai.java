    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        KhuyenMaiDAO cdkm = new KhuyenMaiDAO();
        KhuyenMai km = new KhuyenMai();
        km.setMaNV(txtMaNV.getText());
        km.setMaKM(txtMaKM.getText());
        km.setTenLoaiKM(txtTenKM.getText());
        km.setSoDiemCanDat(Integer.parseInt(txtDiemCanDat.getText()));
        km.setGioHanDoi(Integer.parseInt(txtGioiHanDoi.getText()));
        km.setSoLanDoi(Integer.parseInt(txtSoLanDoi.getText()));
        km.setPhanTram(Float.parseFloat(txtPhanTram.getText()));
        int x = cdkm.themKM(km);
        if(x>0){
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            fillDataToTable();
        }
        else{
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
        }
    }//GEN-LAST:event_btnLuuActionPerformed

