    private void tblKhuyenMaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKhuyenMaiMouseClicked
        int row = tblKhuyenMai.getSelectedRow();
        txtMaNV.setText(tblKhuyenMai.getValueAt(row,0).toString());
        txtMaKM.setText(tblKhuyenMai.getValueAt(row, 1).toString());
        txtTenKM.setText(tblKhuyenMai.getValueAt(row, 2).toString());
        int SoDiemCanDat = Integer.parseInt(tblKhuyenMai.getValueAt(row, 3).toString());
        txtDiemCanDat.setText(String.valueOf(SoDiemCanDat));

        // Assuming GioiHanDoi is an integer
        int GioiHanDoi = Integer.parseInt(tblKhuyenMai.getValueAt(row, 4).toString());
        txtGioiHanDoi.setText(String.valueOf(GioiHanDoi));

        // Assuming SoLanDoi is an integer
        int SoLanDoi = Integer.parseInt(tblKhuyenMai.getValueAt(row, 5).toString());
        txtSoLanDoi.setText(String.valueOf(SoLanDoi));

        // Assuming PhanTram is a float
        float PhanTram = Float.parseFloat(tblKhuyenMai.getValueAt(row, 6).toString());
        txtPhanTram.setText(String.valueOf(PhanTram));
    }//GEN-LAST:event_tblKhuyenMaiMouseClicked

