public void fillDataToTable()
     {
         KhuyenMaiDAO kmDAO = new KhuyenMaiDAO();
         listKM = kmDAO.DocTatCaKhuyenMai();
         tblModel = (DefaultTableModel) tblKhuyenMai.getModel();
         tblModel.setRowCount(0);
         for( KhuyenMai km : listKM)
         {
             Object[] row = new Object[]{km.getMaNV(), km.getMaKM(), km.getTenLoaiKM(), km.getSoDiemCanDat(),km.getGioHanDoi(), km.getSoLanDoi(), km.getPhanTram()};
             tblModel.addRow(row);
             tblKhuyenMai.setModel(tblModel);
         }
     }

