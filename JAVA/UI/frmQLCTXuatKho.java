    public  void filldatatotable ()
    {
        CTXuatKhoDAO ct = new CTXuatKhoDAO();
        listct = ct .DocTatCaKhachHang();
         tblModel = (DefaultTableModel) tblCTXK.getModel();
        tblModel.setRowCount(0);
        
        
        for ( CTXuatKho n : listct)
        {
            Object[] row = new Object[]{n.getMaPX(), n.getMaSP(), n.getSoLuong(),n.getGiamGia(),n.getThanhTien()};
             tblModel.addRow(row);
             tblCTXK.setModel(tblModel);
        }
        
    }
