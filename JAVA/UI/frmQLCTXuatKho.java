    private void LưuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LưuActionPerformed
        CTXuatKhoDAO ctdao = new CTXuatKhoDAO();
        CTXuatKho c = new CTXuatKho();

        c.setMaPX(txtMaPX.getText());
        c.setMaSP(txtMaSP.getText());

        c.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
        c.setGiamGia(Float.parseFloat(txtGiamGIa.getText()));
        c.setThanhTien(Float.parseFloat(txtThanhTien.getText()));

        int x = ctdao.themXK(c);
        if(x>0){
            JOptionPane.showMessageDialog(this, "Sửa thành công");
            filldatatotable();
        }else{
            JOptionPane.showMessageDialog(this, "Sửa thất bại thất bại");
        }

    }//GEN-LAST:event_LưuActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        CTXuatKhoDAO  ctDAO = new CTXuatKhoDAO();
        String MaPX = txtMaPX.getText();
        String MaSP = txtMaSP.getText();

        int x = ctDAO.Xoact(MaPX, MaSP);

        if(x>0){
            filldatatotable();
            JOptionPane.showMessageDialog(this, "Xóa thành công");
            clear();
        }
        else{
            JOptionPane.showMessageDialog(this, "Xóa thành công");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

