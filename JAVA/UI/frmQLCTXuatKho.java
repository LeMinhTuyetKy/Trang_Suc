    private void tblCTXKMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCTXKMouseClicked
        int row = tblCTXK.getSelectedRow();
        txtMaPX.setText(tblCTXK.getValueAt(row,0).toString());
        txtMaSP.setText(tblCTXK.getValueAt(row,1).toString());

        int SoLuong = Integer.parseInt(tblCTXK.getValueAt(row, 2).toString()); // Assuming diem is at index 5
        txtSoLuong.setText(String.valueOf(SoLuong));

        float GiamGia = Float.parseFloat(tblCTXK.getValueAt(row, 3).toString());
        txtGiamGIa.setText(String.valueOf(GiamGia));

        float ThanhTien = Float.parseFloat(tblCTXK.getValueAt(row, 4).toString());
        txtThanhTien.setText(String.valueOf(ThanhTien));
    }//GEN-LAST:event_tblCTXKMouseClicked


